USE WAREHOUSE RETAIL_WH;
USE DATABASE RETAIL_ANALYTICS_DB;
USE SCHEMA REPORTING;

-- Total Business Revenue

SELECT
    ROUND(SUM(SALES_AMOUNT),2) AS TOTAL_REVENUE
FROM MART.FACT_SALES;

-- Total Orders

SELECT
    COUNT(SALES_ID) AS TOTAL_ORDERS
FROM MART.FACT_SALES;

-- Average Order Value

SELECT
    ROUND(AVG(SALES_AMOUNT),2) AS AVG_ORDER_VALUE
FROM MART.FACT_SALES;

-- Top 10 Products by Revenue

SELECT
    PRODUCT_NAME,
    CATEGORY,
    TOTAL_REVENUE
FROM REPORTING.VW_PRODUCT_PERFORMANCE
ORDER BY TOTAL_REVENUE DESC
LIMIT 10;

-- Top 10 Customers

SELECT
    CUSTOMER_ID,
    FIRST_NAME,
    LAST_NAME,
    LIFETIME_VALUE
FROM REPORTING.VW_CUSTOMER_ANALYTICS
ORDER BY LIFETIME_VALUE DESC
LIMIT 10;

-- Top Stores
SELECT
    STORE_NAME,
    REGION,
    TOTAL_REVENUE
FROM REPORTING.VW_STORE_PERFORMANCE
ORDER BY TOTAL_REVENUE DESC;

-- Revenue by Category

SELECT

P.CATEGORY,

ROUND(SUM(F.SALES_AMOUNT),2) AS REVENUE

FROM MART.FACT_SALES F

JOIN MART.DIM_PRODUCTS P
ON F.PRODUCT_ID=P.PRODUCT_ID

GROUP BY P.CATEGORY

ORDER BY REVENUE DESC;

-- Revenue by State

SELECT

S.STATE,

ROUND(SUM(F.SALES_AMOUNT),2) AS REVENUE

FROM MART.FACT_SALES F

JOIN MART.DIM_STORES S
ON F.STORE_ID=S.STORE_ID

GROUP BY S.STATE

ORDER BY REVENUE DESC;

-- Monthly Revenue Trend

SELECT *

FROM REPORTING.VW_MONTHLY_SALES

ORDER BY YEAR,MONTH;

-- Top 5 Brands

SELECT

P.BRAND,

ROUND(SUM(F.SALES_AMOUNT),2) AS REVENUE

FROM MART.FACT_SALES F

JOIN MART.DIM_PRODUCTS P

ON F.PRODUCT_ID=P.PRODUCT_ID

GROUP BY P.BRAND

ORDER BY REVENUE DESC

LIMIT 5;

-- Customer Segments

SELECT

CUSTOMER_SEGMENT,

COUNT(*) AS TOTAL_CUSTOMERS

FROM MART.DIM_CUSTOMERS

GROUP BY CUSTOMER_SEGMENT

ORDER BY TOTAL_CUSTOMERS DESC;

-- Revenue by Payment Method

SELECT

PAYMENT_METHOD,

ROUND(SUM(SALES_AMOUNT),2) AS REVENUE

FROM MART.FACT_SALES

GROUP BY PAYMENT_METHOD

ORDER BY REVENUE DESC;

-- Running Total Revenue

SELECT

DATE_KEY,

SUM(SALES_AMOUNT) AS DAILY_REVENUE,

SUM(SUM(SALES_AMOUNT)) OVER
(
ORDER BY DATE_KEY
) AS RUNNING_REVENUE

FROM MART.FACT_SALES

GROUP BY DATE_KEY

ORDER BY DATE_KEY;

-- Rank Products by Revenue

SELECT

PRODUCT_NAME,

SUM(F.SALES_AMOUNT) AS REVENUE,

RANK() OVER
(
ORDER BY SUM(F.SALES_AMOUNT) DESC
) AS PRODUCT_RANK

FROM MART.FACT_SALES F

JOIN MART.DIM_PRODUCTS P

ON F.PRODUCT_ID=P.PRODUCT_ID

GROUP BY PRODUCT_NAME;

-- Top Product in Every Category

WITH PRODUCT_REVENUE AS
(
SELECT

CATEGORY,

PRODUCT_NAME,

SUM(F.SALES_AMOUNT) AS REVENUE,

ROW_NUMBER() OVER
(
PARTITION BY CATEGORY
ORDER BY SUM(F.SALES_AMOUNT) DESC
) RN

FROM MART.FACT_SALES F

JOIN MART.DIM_PRODUCTS P

ON F.PRODUCT_ID=P.PRODUCT_ID

GROUP BY
CATEGORY,
PRODUCT_NAME
)

SELECT *

FROM PRODUCT_REVENUE

WHERE RN=1;


